import type { InspirationCard } from '../types';

export interface ThemedPack {
  id: string;
  name: { en: string; he: string };
  icon: string;
  cards: InspirationCard[];
  localeOnly?: string;
}

function artistCard(id: string, artist: string, heArtist?: string, copies = 1): InspirationCard {
  const he = heArtist || artist;
  return {
    id, color: 'yellow',
    instruction: {
      en: `A song by ${artist}`,
      he: `שיר של ${he}`,
      es: `A song by ${artist}`,
      fr: `A song by ${artist}`,
    },
    bonusPoints: 5, copies, exclusionGroup: 'artist',
  };
}

function heOnlyArtistCard(id: string, heArtist: string): InspirationCard {
  return {
    id, color: 'yellow',
    instruction: {
      en: `A song by ${heArtist}`,
      he: `שיר של ${heArtist}`,
      es: `A song by ${heArtist}`,
      fr: `A song by ${heArtist}`,
    },
    bonusPoints: 5, copies: 1, exclusionGroup: 'artist',
  };
}

